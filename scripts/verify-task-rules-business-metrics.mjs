import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const contract = JSON.parse(read('config/task-rules-business-metrics-contract.json'))
const schema = read('server/prisma/schema.prisma')
const migration = read('server/prisma/migrations/0067_task_rules_business_metrics/migration.sql')
const configRuntime = read('server/src/configResources/configResourceRuntime.js')
const configRepository = read('server/src/configResources/prismaConfigResourcesRepository.js')
const taskRuntime = read('server/src/tasks/taskRuleRuntime.js')
const taskRepository = read('server/src/repositories/prismaRepository.js')
const metricsRepository = read('server/src/tasks/prismaTaskAdminRepository.js')
const configRoutes = read('server/src/modules/configResources/routes.js')
const taskRoutes = read('server/src/modules/tasks/routes.js')
const permissions = read('server/src/auth/permissions.js')
const openapi = read('server/src/docs/openapi.js')
const configPanel = read('src/features/admin/ConfigurationResourcesPanel.tsx')
const taskPanel = read('src/features/admin/TaskAdminPanel.tsx')
const publishPage = read('src/features/tasks/TaskPages.tsx')
const packageJson = JSON.parse(read('package.json'))
const checks = []
const add = (name, pass, detail = '') => checks.push({ name, pass: Boolean(pass), detail })

add('contract is TASK-03 personal-account scope', contract.taskId === 'TASK-03' && contract.scope === 'personal_accounts_only')
add('task_rule is a managed config kind', schema.includes('task_rule') && configRuntime.includes("'task_rule'"))
add('TaskRule projection is normalized', /model TaskRule[\s\S]*?acceptanceTemplates[\s\S]*?defaultDeadlineHours[\s\S]*?publishedVersion/.test(schema))
add('migration installs projection and deadline constraints', migration.includes('CREATE TABLE "task_rules"') && migration.includes('task_rules_deadline_range_check'))
add('migration installs dedicated grants', contract.permissions.slice(0, 3).every((permission) => migration.includes(`'${permission}'`)))
add('permission registry exposes dedicated controls', contract.permissions.slice(0, 3).every((permission) => permissions.includes(`'${permission}'`)))
add('rule schema validates every governed field', contract.ruleFields.every((field) => configRuntime.includes(field)))
add('publication writes the TaskRule projection', configRepository.includes('transaction.taskRule.upsert'))
add('published rule reads are active and deletion aware', configRepository.includes('listPublishedTaskRules') && configRepository.includes('findPublishedTaskRule'))
add('task creation applies published rules', taskRepository.includes('applyPublishedTaskRule') && taskRuntime.includes('TASK_CATEGORY_UNAVAILABLE'))
add('template and deadline failures are explicit', ['TASK_ACCEPTANCE_TEMPLATE_INVALID', 'TASK_DEADLINE_OUT_OF_RANGE'].every((code) => taskRuntime.includes(code)))
add('task creation records rule version evidence', taskRuntime.includes('publishedVersion') && taskRepository.includes('taskRule: governedPayload.taskRule'))
add('authenticated rule route is implemented', configRoutes.includes("router.add('GET', '/api/task-rules'"))
add('business metrics route is implemented', taskRoutes.includes("router.add('GET', '/api/admin/tasks/business-metrics'"))
add('business metrics export is implemented', taskRoutes.includes("router.add('GET', '/api/admin/tasks/business-metrics/export'"))
add('business metrics enforce read permission', taskRoutes.includes("requirePermission(context, 'admin:tasks:read')"))
add('metrics cover funnel deadlines and disputes', contract.metrics.groups.every((group) => metricsRepository.includes(`${group}:`)))
add('metrics use server-side aggregate SQL', metricsRepository.includes('WITH scoped_tasks AS') && metricsRepository.includes('COUNT(*) FILTER'))
add('metrics export has a stable kind', taskRoutes.includes(contract.metrics.exportKind))
add('Admin config panel manages task rules', configPanel.includes("kind: 'task_rule'") && configPanel.includes("'admin:task-rules:publish'"))
add('publisher UI consumes category rules and templates', publishPage.includes('taskService.rules()') && publishPage.includes('acceptanceTemplates'))
add('task Admin UI filters and exports metrics', taskPanel.includes('taskBusinessMetrics') && taskPanel.includes('exportTaskBusinessMetrics'))
for (const route of contract.routes) add(`${route.method} ${route.path} is documented`, openapi.includes(`'${route.path.replace('/api', '')}'`))
add('policy document exists', fs.existsSync(path.join(root, 'docs/TASK_RULES_AND_BUSINESS_METRICS.md')))
add('package exposes TASK-03 gate', packageJson.scripts['test:task-rules-business-metrics']?.includes('verify-task-rules-business-metrics.mjs'))
add('quick gate includes TASK-03', packageJson.scripts['check:quick'].includes('npm run test:task-rules-business-metrics'))
add('forbidden shared-account models remain absent', contract.tenantModels === false && !/model (Tenant|Organization|Team|Workspace|Membership|Invitation)\b/.test(schema))

for (const check of checks) console.log(`${check.pass ? 'PASS' : 'FAIL'} ${check.name}${check.detail ? ` (${check.detail})` : ''}`)
const failures = checks.filter((check) => !check.pass)
if (failures.length) {
  console.error(`TASK-03 verification failed: ${failures.length} check(s)`)
  process.exitCode = 1
} else console.log(`TASK-03 verified: ${checks.length} checks`)
