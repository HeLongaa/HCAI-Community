import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const contract = JSON.parse(read('config/media-capacity-scan-metrics-contract.json'))
const schema = read('server/prisma/schema.prisma')
const parser = read('server/src/contracts/requestParsers.js')
const runtime = read('server/src/media/mediaBusinessMetrics.js')
const prismaMetrics = read('server/src/media/prismaMediaBusinessMetricsRepository.js')
const prismaRepository = read('server/src/repositories/prismaRepository.js')
const seedRepository = read('server/src/repositories/seedRepository.js')
const routes = read('server/src/modules/media/routes.js')
const openapi = read('server/src/docs/openapi.js')
const permissions = read('server/src/auth/permissions.js')
const panel = read('src/features/admin/AdminMediaLifecyclePanel.tsx')
const service = read('src/services/mediaService.ts')
const packageJson = JSON.parse(read('package.json'))
const checks = []
const add = (name, pass) => checks.push({ name, pass: Boolean(pass) })

add('contract owns MEDIA-04 personal-account scope', contract.taskId === 'MEDIA-04' && contract.scope === 'personal_accounts_only')
add('metrics reuse normalized media sources without schema duplication', contract.schemaChange === false && contract.sourceModels.every((model) => schema.includes(`model ${model}`)))
add('metrics filters are frozen and bounded', contract.filters.every((field) => parser.includes(field)) && parser.includes('metrics window cannot exceed 366 days'))
add('seed runtime covers every metric group', contract.groups.every((group) => runtime.includes(`${group}:`)))
add('seed runtime computes failure rate and latency percentile', runtime.includes('failurePercent') && runtime.includes('p95LatencySeconds'))
add('seed repository exposes media business metrics', seedRepository.includes('businessMetrics:') && seedRepository.includes('buildMediaBusinessMetrics'))
add('Prisma repository exposes media business metrics', prismaRepository.includes('createPrismaMediaBusinessMetricsRepository'))
add('Prisma metrics use server-side aggregate SQL', prismaMetrics.includes('PERCENTILE_CONT(0.95)') && prismaMetrics.includes('COUNT(*) FILTER'))
add('Prisma metrics synthesize timed-out backlog', prismaMetrics.includes("THEN 'timed_out'") && prismaMetrics.includes('oldest_backlog_at'))
add('read route uses dedicated media read permission', routes.includes("'/api/admin/media/business-metrics'") && routes.includes("requirePermission(context, 'admin:media:read')"))
add('export route uses dedicated media export permission', routes.includes("'/api/admin/media/business-metrics/export'") && routes.includes("requirePermission(context, 'admin:media:export')"))
add('metrics reads and exports are audited', routes.includes('admin.media.business_metrics.queried') && routes.includes('admin.media.business_metrics.exported'))
add('metrics export has a stable artifact kind', routes.includes(contract.exportKind))
add('permissions remain registered', contract.permissions.every((permission) => permissions.includes(`'${permission}'`)))
for (const route of contract.routes) add(`${route.method} ${route.path} is documented`, openapi.includes(`'${route.path.replace('/api', '')}'`))
add('frontend service exposes read and export methods', service.includes('adminBusinessMetrics(') && service.includes('adminBusinessMetricsExport('))
add('Admin media panel renders capacity, failure, latency and backlog', ['capacity.activeBytes', 'scan.failurePercent', 'scan.p95LatencySeconds', 'backlog.total'].every((marker) => panel.includes(marker)))
add('Admin media panel filters purpose, type and dates', panel.includes('metricsDates') && panel.includes('query.mediaType') && panel.includes('query.purpose'))
add('Admin media panel exports metrics evidence', panel.includes('media-business-metrics.json'))
add('forbidden raw evidence is absent from the aggregate projection', contract.forbiddenEvidence.every((field) => !prismaMetrics.includes(`${field}:`)))
add('policy document exists', fs.existsSync(path.join(root, 'docs/MEDIA_CAPACITY_SCAN_METRICS.md')))
add('package exposes MEDIA-04 gate', packageJson.scripts['test:media-capacity-scan-metrics']?.includes('verify-media-capacity-scan-metrics.mjs'))
add('quick gate includes MEDIA-04', packageJson.scripts['check:quick'].includes('npm run test:media-capacity-scan-metrics'))
add('real Provider calls remain disabled', contract.realProviderCalls === false)
add('forbidden shared-account models remain absent', contract.tenantModels === false && !/model (Tenant|Organization|Team|Workspace|Membership|Invitation)\b/.test(schema))

for (const check of checks) console.log(`${check.pass ? 'PASS' : 'FAIL'} ${check.name}`)
const failures = checks.filter((check) => !check.pass)
if (failures.length) {
  console.error(`MEDIA-04 verification failed: ${failures.length} check(s)`)
  process.exitCode = 1
} else console.log(`MEDIA-04 verified: ${checks.length} checks`)
