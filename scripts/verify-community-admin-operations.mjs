import fs from 'node:fs'

const read = (path) => fs.readFileSync(path, 'utf8')
const json = (path) => JSON.parse(read(path))
const contract = json('config/community-admin-operations-contract.json')
const schema = read('server/prisma/schema.prisma')
const migration = read('server/prisma/migrations/0083_community_admin_operations/migration.sql')
const routes = read('server/src/modules/communityAdmin/routes.js')
const runtime = read('server/src/community/communityAdminContract.js')
const prisma = read('server/src/community/prismaCommunityAdminRepository.js')
const ui = read('src/features/admin/CommunityAdminPanel.tsx')
const openapi = read('server/src/docs/openapi.js')
const permissions = read('server/src/auth/permissions.js')
const packageJson = json('package.json')
const checks = []
const add = (name, pass) => checks.push({ name, pass: Boolean(pass) })

add('COMM-03 contract is supported', contract.schemaVersion === 1 && contract.task === 'COMM-03')
add('comments have CAS and soft-delete evidence', /model Comment[\s\S]*?version[\s\S]*?updatedAt[\s\S]*?deletedAt[\s\S]*?deletionReasonCode/.test(schema))
add('bulk operation evidence is schema-versioned', /model CommunityAdminBulkOperation[\s\S]*?idempotencyKey[\s\S]*?targetHash[\s\S]*?result[\s\S]*?resultSchemaVersion/.test(schema))
add('migration installs constraints, indexes, and grants', migration.includes('community_admin_bulk_operations_action_check') && migration.includes('comments_post_id_deleted_at_moderation_state_created_at_id_idx') && migration.includes('admin:community:manage'))
add('dedicated permissions are registered', ['admin:community:read', 'admin:community:manage', 'admin:community:export'].every((id) => permissions.includes(`'${id}'`)))
add('post and comment CRUD routes are implemented', ['posts', 'comments'].every((type) => routes.includes(`/api/admin/community/${type}`)) && routes.includes("/delete'") && routes.includes("/restore'"))
add('metrics and export routes are implemented', routes.includes("'/api/admin/community/metrics'") && routes.includes("'/api/admin/community/metrics/export'"))
add('bulk preview and execution are implemented', routes.includes("'/api/admin/community/bulk/preview'") && routes.includes("'/api/admin/community/bulk'"))
add('bulk is hash, confirmation, and idempotency bound', runtime.includes('hashCommunityTargets') && runtime.includes('communityConfirmationText') && prisma.includes('idempotencyKey'))
add('Admin lifecycle does not mutate Trust moderation state', contract.softDeleteSeparateFromTrustModeration && !/data\s*=\s*[^\n]*moderationState/.test(prisma))
add('metrics contain aggregates without raw content', contract.metrics.rawContentForbidden && !/metadata:[^\n]*(body|title)/.test(routes))
add('responsive Community Admin UI is present', ui.includes('community-admin-panel') && ui.includes('task-admin-workspace') && ui.includes('previewCommunityBulk'))
add('OpenAPI documents the complete route group', ['/admin/community/posts', '/admin/community/comments', '/admin/community/metrics', '/admin/community/bulk'].every((path) => openapi.includes(`'${path}'`)))
add('focused gate is exposed', packageJson.scripts['test:community-admin-operations']?.includes('verify-community-admin-operations.mjs'))

for (const check of checks) console.log(`${check.pass ? 'PASS' : 'FAIL'} ${check.name}`)
const failures = checks.filter((check) => !check.pass)
if (failures.length) { console.error(`Community Admin verification failed: ${failures.length} check(s)`); process.exitCode = 1 } else console.log(`Community Admin verified: ${checks.length} checks`)
