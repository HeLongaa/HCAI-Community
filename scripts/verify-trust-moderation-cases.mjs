import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const json = (file) => JSON.parse(read(file))
const contract = json('config/trust-moderation-cases-contract.json')
const pkg = json('package.json')
const schema = read('server/prisma/schema.prisma')
const migration = read(`server/prisma/migrations/${contract.migration}/migration.sql`)
const domain = read('server/src/trust/moderationCases.js')
const seed = read('server/src/trust/seedModerationCaseRepository.js')
const prisma = read('server/src/trust/prismaModerationCaseRepository.js')
const routes = read('server/src/modules/trust/routes.js')
const supportRoutes = read('server/src/modules/compliance/routes.js')
const openapi = read('server/src/docs/openapi.js')
const permissions = read('server/src/auth/permissions.js')
const ui = read('src/features/admin/TrustSafetyAdminPanel.tsx')
const supportUi = read('src/features/static-pages/StaticPages.tsx')
const policies = json('config/entity-operation-policies.json')
const lists = json('config/list-query-contract.json')
const resources = json('config/admin-resource-framework-contract.json')
const audit = json('config/admin-mutation-audit.json')
const boundaries = json('config/domain-boundaries.json')
const navigation = json('config/route-navigation-contract.json')

const checks = []
const add = (name, pass) => checks.push({ name, pass: Boolean(pass) })
add('contract is TRUST-01 personal-account scope', contract.task === 'TRUST-01' && contract.scope === 'personal_accounts_only' && contract.operationPolicy === 'append_only')
for (const model of contract.models) add(`${model} is modeled`, schema.includes(`model ${model}`))
for (const table of ['trust_reports', 'moderation_cases', 'moderation_evidence', 'moderation_decisions', 'moderation_appeals']) add(`${table} is migrated`, migration.includes(`"${table}"`))
add('database triggers reject fact updates and deletes', migration.includes('reject_moderation_fact_mutation') && (migration.match(/_immutable BEFORE UPDATE OR DELETE/g) ?? []).length === 5)
add('migration enforces one original decision and one appeal', migration.includes('moderation_decisions_case_id_stage_key') && migration.includes('moderation_appeals_case_id_key'))
add('migration enforces stage-specific outcomes', migration.includes('moderation_decisions_stage_outcome_check'))
add('domain derives case status and version', domain.includes('moderationCaseState') && domain.includes('moderationCaseVersion'))
add('domain enforces 30-day appeal window', domain.includes('30 * 24 * 60 * 60 * 1000'))
add('domain rejects credential-like text', domain.includes('SENSITIVE_MODERATION_CONTENT'))
add('seed and Prisma enforce independent appeal review', [seed, prisma].every((source) => source.includes('INDEPENDENT_REVIEW_REQUIRED')))
add('seed and Prisma validate targets and idempotency', [seed, prisma].every((source) => source.includes('MODERATION_TARGET_NOT_FOUND') && source.includes('REPORT_SOURCE_CONFLICT')))
for (const route of [...contract.userRoutes, ...contract.adminRoutes]) {
  add(`${route.method} ${route.path} is implemented`, routes.includes(`router.add('${route.method}', '${route.path}'`))
  add(`${route.method} ${route.path} is documented`, openapi.includes(`'${route.path.replace('/api', '').replace(/:([A-Za-z]+)/g, '{$1}')}'`))
}
add('generic support queue rejects reports and appeals', supportRoutes.includes('DEDICATED_TRUST_ROUTE_REQUIRED'))
for (const permission of contract.permissions) add(`${permission} is registered`, permissions.includes(`'${permission}'`))
add('Admin UI covers metrics filters fact detail decision evidence and export', ['adminMetrics', 'adminExport', 'Moderation outcome', 'Evidence content hash', 'Fact chain'].every((marker) => ui.includes(marker)))
add('Support UI uses dedicated Trust report and appeal APIs', supportUi.includes('trustService.createReport') && supportUi.includes('trustService.appeal'))
for (const model of contract.models) add(`${model} operation policy is append-only`, policies.entities.some((entry) => entry.model === model && entry.policy === 'append_only'))
add('moderation case list and export contract is registered', lists.resources.some((entry) => entry.id === 'moderationCases' && entry.export === true))
add('moderation case Admin resource is registered', resources.resources.some((entry) => entry.id === 'moderationCases'))
add('Trust route module is registered to the trust-safety-risk domain', boundaries.routeModules.some((entry) => entry.id === 'trust' && entry.domain === 'trust-safety-risk' && entry.registration === 'registerTrustRoutes'))
add('Trust user routes map to the Support navigation surface', navigation.routeGroups.some((entry) => entry.id === 'trust' && entry.module === 'trust-safety-risk' && entry.prefixes.includes('/api/trust') && entry.nav === 'support'))
for (const route of contract.adminRoutes.filter((entry) => entry.method === 'POST')) add(`${route.path} is domain audited`, audit.routes.some((entry) => entry.path === route.path && entry.mode === 'domain_audited'))
add('runbook exists', fs.existsSync(path.join(root, 'docs/TRUST_MODERATION_CASES.md')))
add('focused package gate exists', pkg.scripts['test:trust-moderation-cases']?.includes('verify-trust-moderation-cases.mjs'))
add('integration package gate exists', pkg.scripts['test:trust-moderation-cases:integration']?.includes('prismaModerationCases.integration.test.js'))
add('quick gate includes TRUST-01', pkg.scripts['precheck:quick']?.includes('test:trust-moderation-cases'))

for (const check of checks) console.log(`${check.pass ? 'PASS' : 'FAIL'} ${check.name}`)
const failures = checks.filter((check) => !check.pass)
if (failures.length) { console.error(`Trust moderation cases verification failed: ${failures.length} check(s)`); process.exitCode = 1 }
else console.log(`Trust moderation cases verified: ${checks.length} checks`)
